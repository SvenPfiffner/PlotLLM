import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt
import numpy as np
import io

from chat import assistant_response, check_service

# Set up Streamlit page
st.set_page_config(page_title="PlotLLM: AI Plot Generator", layout="wide")

# Disclaimer
